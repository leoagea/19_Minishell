#include "../../../inc/libft.h"

void	dll_print_backward(t_dll *dll)
{
	t_node	*current;

	current = dll->tail;
	while (current != NULL)
	{
		ft_printf("value : %d\n", current->value);
		current = current->prev;
	}
}
